#include "led_toggle_task.h"

// --------------------------------------------------------------------------
// static handle
// --------------------------------------------------------------------------
static LED_HANDLE s_led_handle = {.gpiox = GPIOA, .gpio_pin = GPIO_PIN_6};

// --------------------------------------------------------------------------
// Task
// --------------------------------------------------------------------------
void led_toggle_task(void *argument) {
  while (1) {
    led_toggle(&s_led_handle);
    vTaskDelay(500);
  }
}

